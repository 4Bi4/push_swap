int	str_check(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9') || str[i] != ' ')
			return (write(2, "Error\n", 7), 1);
		i++;
	}
	return (0);
}

